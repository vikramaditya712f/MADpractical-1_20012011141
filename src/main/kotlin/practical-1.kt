fun main()
{
    var a : Int = 2147483647
    val b : Float = 22.2F
    val c : Char = 'S'
    val d : String = "LCA"
    val e : Boolean = false
    val f : Double = 22.2
    val g : Long = 2147483648
    val h : Short = -32768
    val i : Byte = 127

    println("Integer = $a")
    println("Float = $b")
    println("Character = $c")
    println("String = $d")
    println("Boolean = $e")
    println("Double = $f")
    println("Long = $g")
    println("Short = $h")
    println("Byte = $i")
}
